#!/bin/bash
# Скрипт для остановки Django-сервера и Docker-контейнеров

# Переходим в директорию проекта (адаптируйте путь при необходимости)
PROJECT_DIR="mygis-project/mygis"

echo "Останавливаем сервер Django и Docker-контейнеры..."
cd "$(dirname "$0")/${PROJECT_DIR}" 2>/dev/null || { echo "Директория проекта не найдена!"; exit 1; }

# Останавливаем и удаляем контейнеры
docker-compose down

echo "Все контейнеры успешно остановлены и удалены"